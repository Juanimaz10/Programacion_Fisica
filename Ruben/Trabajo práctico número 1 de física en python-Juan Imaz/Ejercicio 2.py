# Conversión de unidades para el volumen de 55 m³

# Dato del volumen en metros cúbicos
volumen_m3 = 55

# Conversión a centímetros cúbicos (1 m³ = 1e6 cm³)
volumen_cm3 = volumen_m3 * 1e6

# Conversión a pulgadas cúbicas (1 m³ = 61023.7441 in³)
volumen_in3 = volumen_m3 * 61023.7441

# Conversión a pies cúbicos (1 m³ = 35.3147 ft³)
volumen_ft3 = volumen_m3 * 35.3147

# Conversión a hectolitros (1 m³ = 10 hectolitros)
volumen_hectolitros = volumen_m3 * 10

# Resultados
print(f"Volumen en cm³: {volumen_cm3}")
print(f"Volumen en in³: {volumen_in3}")
print(f"Volumen en ft³: {volumen_ft3}")
print(f"Volumen en hectolitros: {volumen_hectolitros}")
