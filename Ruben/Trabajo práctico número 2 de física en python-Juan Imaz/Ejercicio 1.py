# Definimos las velocidades en m/s (por ejemplo)
v1 = 20  # Velocidad del auto 1 en m/s
v2 = 25  # Velocidad del auto 2 en m/s

# Distancia inicial
d = 2000  # Distancia en metros

# Tiempo hasta el encuentro
t = d / (v1 + v2)

# Mostramos el resultado
print(f"El tiempo en que se encuentran los autos es de {t:.2f} segundos.")
